#4  What's expression made of? What expressions do?
# An expression is a combination of operators and operands that is interpreted to produce some other value. In any programming language, an expression is evaluated as per the precedence of its operators. So that if there is more than one operator in an expression, their precedence decides which operation will be performed first. We have many different types of expressions in Python.
# Constant Expressions: These are the expressions that have constant values only.
# Arithmetic Expressions: An arithmetic expression is a combination of numeric values, operators, and sometimes parenthesis. The result of this type of expression is also a numeric value. The operators used in these expressions are arithmetic operators like addition, subtraction, etc.
# Integral Expressions: These are the kind of expressions that produce only integer results after all computations and type conversions.
# Floating Expressions: These are the kind of expressions which produce floating point numbers as result after all computations and type conversions.
# Relational Expressions: In these types of expressions, arithmetic expressions are written on both sides of relational operator (> , < , >= , <=). Those arithmetic expressions are evaluated first, and then compared as per relational operator and produce a boolean output in the end. These expressions are also called Boolean expressions.
# Logical Expressions: These are kinds of expressions that result in either True or False. It basically specifies one or more conditions. For example, (10 == 9) is a condition if 10 is equal to 9. As we know it is not correct, so it will return False. Studying logical expressions, we also come across some logical operators which can be seen in logical expressions most often.
# Bitwise Expressions: These are the kind of expressions in which computations are performed at bit level.
# Combinational Expressions: We can also use different types of expressions in a single expression, and that will be termed as combinational expressions.


